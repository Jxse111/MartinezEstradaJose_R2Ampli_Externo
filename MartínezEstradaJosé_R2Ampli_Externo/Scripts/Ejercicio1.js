var numero1 = parseInt(prompt("Introduzca un primer numero"));
var numero2 = parseInt(prompt("Introduzca un segundo numero"));
var numero3 = parseInt(prompt("Introduzca un tercer numero"));
var media;
media = (numero1 + numero2 + numero3) / 3;
document.writeln("<p>El primer número introducido es: " + numero1 + "</p>");
document.writeln("<p>El segundo número introducido es: " + numero2 + "</p>");
document.writeln("<p>El tercero número introducido es: " + numero3 + "</p>");
document.writeln("<p>El resultado de la media es: " + media + "</p>");
