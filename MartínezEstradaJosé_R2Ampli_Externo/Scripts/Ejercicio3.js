numero1 = parseInt(prompt("Introduce un primer número: "));
numero2 = parseInt(prompt("Introduce un segundo número"));
if (numero1 > numero2) {
    document.write("<p>El numero 1 es el mayor de los dos con un valor: " + numero1 + "</p>");
} else {
    document.write("<p>El numero 2 es el mayor de los dos con un valor: " + numero2 + "</p>");
}